export class UserObject {
  constructor(
    id,
    username,
    firstName,
    lastName,
    age,
    gender,
    city,
    state,
    role
  ) {
    this.id = id;
    this.username = username;
    this.firstName = firstName;
    this.lastName = lastName;
    this.age = age;
    this.gender = gender;
    this.city = city;
    this.state = state;
    this.role = role;
  }
}
